package ch_03;

public class _02_escapeSequence {
    public static void main(String[] args) {
        //특수문자
        // \n : 줄바꿈
        System.out.println("신기한\n세계");
        // \t : 탭
        System.out.println("신기한\t세계");
        // \\ : 역슬래시 출력
        System.out.println("c:\\game\\window");
        // \" \' : 따옴표 출력
        System.out.println("강아지가 \"왈왈\" 짖고 있습니다");
    }
}
